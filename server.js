const WebSocket = require("ws")
const http = require("http")
const fs = require("fs")
const path = require("path")

// Crea server HTTP
const server = http.createServer((req, res) => {
  if (req.url === "/" || req.url === "/index.html") {
    fs.readFile(path.join(__dirname, "index.html"), (err, data) => {
      if (err) {
        res.writeHead(404)
        res.end("File not found")
        return
      }
      res.writeHead(200, { "Content-Type": "text/html" })
      res.end(data)
    })
  } else {
    res.writeHead(404)
    res.end("Not found")
  }
})

// Crea WebSocket server
const wss = new WebSocket.Server({ server })

// Game state
const rooms = new Map()
const players = new Map()

// Utility functions
function generateRoomCode() {
  return "ROOM" + Math.random().toString(36).substr(2, 4).toUpperCase()
}

function generateSecretNumber() {
  return Math.floor(Math.random() * 100) + 1
}

function createRoom(playerId, playerName) {
  const roomCode = generateRoomCode()
  const room = {
    roomCode,
    players: [
      {
        id: playerId,
        name: playerName,
        score: 0,
        isHost: true,
      },
    ],
    gameStarted: false,
    secretNumber: null,
    chat: [],
    currentRound: 0,
    currentPlayerIndex: 0,
    turnTimeLimit: 30,
    turnStartTime: null,
    turnTimer: null,
    maxAttempts: 3,
    currentAttempts: 0,
  }

  rooms.set(roomCode, room)
  console.log(`‚úÖ Stanza creata: ${roomCode}, giocatori: ${room.players.length}`)
  return room
}

function addPlayerToRoom(roomCode, playerId, playerName) {
  const room = rooms.get(roomCode)
  if (!room) {
    console.log(`‚ùå Stanza ${roomCode} non trovata`)
    return null
  }

  // Controlla se il giocatore √® gi√† nella stanza
  const existingPlayer = room.players.find((p) => p.id === playerId)
  if (existingPlayer) {
    console.log(`‚ö†Ô∏è Giocatore ${playerName} gi√† nella stanza ${roomCode}`)
    return room
  }

  // Aggiungi nuovo giocatore
  room.players.push({
    id: playerId,
    name: playerName,
    score: 0,
    isHost: false,
  })

  console.log(`‚úÖ ${playerName} aggiunto alla stanza ${roomCode}, totale giocatori: ${room.players.length}`)

  // Aggiungi messaggio di benvenuto alla chat
  room.chat.push({
    playerName: "Sistema",
    message: `üöÄ ${playerName} si √® unito alla battaglia!`,
    timestamp: Date.now(),
  })

  return room
}

function broadcastToRoom(roomCode, message, excludePlayerId = null) {
  const room = rooms.get(roomCode)
  if (!room) return

  console.log(`üì° Broadcasting a stanza ${roomCode}, giocatori: ${room.players.length}`)

  room.players.forEach((player) => {
    if (player.id !== excludePlayerId) {
      const ws = players.get(player.id)
      if (ws && ws.readyState === WebSocket.OPEN) {
        try {
          ws.send(JSON.stringify(message))
        } catch (error) {
          console.error(`‚ùå Errore invio a ${player.name}:`, error)
        }
      }
    }
  })
}

function startGame(roomCode) {
  console.log(`üéÆ Tentativo di avvio gioco per stanza: ${roomCode}`)

  const room = rooms.get(roomCode)
  if (!room) {
    console.log(`‚ùå Stanza ${roomCode} non trovata per startGame`)
    return { error: "Stanza non trovata" }
  }

  console.log(`üìä Stanza ${roomCode} - Giocatori: ${room.players.length}, Gi√† iniziato: ${room.gameStarted}`)

  if (room.players.length < 2) {
    console.log(`‚ùå Non abbastanza giocatori: ${room.players.length}`)
    return { error: "Servono almeno 2 giocatori per iniziare!" }
  }

  if (room.gameStarted) {
    console.log(`‚ö†Ô∏è Gioco gi√† iniziato per stanza ${roomCode}`)
    return { error: "Il gioco √® gi√† iniziato!" }
  }

  try {
    console.log(`üöÄ Avvio gioco per stanza ${roomCode}`)

    room.gameStarted = true
    room.secretNumber = generateSecretNumber()
    room.currentRound = 1
    room.currentPlayerIndex = 0
    room.currentAttempts = 0
    room.turnStartTime = Date.now()

    console.log(`üéØ Numero segreto generato: ${room.secretNumber}`)
    console.log(`üë§ Primo giocatore: ${room.players[0].name}`)

    const currentPlayer = room.players[room.currentPlayerIndex]

    room.chat.push({
      playerName: "Sistema",
      message: `üéØ Battaglia iniziata! Numero segreto generato (1-100)`,
      timestamp: Date.now(),
    })

    room.chat.push({
      playerName: "Sistema",
      message: `üéÆ √à il turno di ${currentPlayer.name}! (${room.turnTimeLimit}s, max ${room.maxAttempts} tentativi)`,
      timestamp: Date.now(),
    })

    console.log(`‚úÖ Gioco avviato con successo per stanza ${roomCode}`)
    return { success: true }
  } catch (error) {
    console.error(`‚ùå Errore in startGame per stanza ${roomCode}:`, error)
    return { error: "Errore interno del server: " + error.message }
  }
}

// WebSocket connection handler
wss.on("connection", (ws) => {
  console.log("üîó Nuova connessione WebSocket")

  // Invia conferma di connessione
  ws.send(
    JSON.stringify({
      type: "connected",
      message: "Connesso al server!",
    }),
  )

  ws.on("message", (data) => {
    try {
      const message = JSON.parse(data)
      console.log(`üì® Messaggio ricevuto: ${message.type}`, message)

      switch (message.type) {
        case "createRoom":
          {
            console.log(`üè† Creazione stanza per ${message.playerName}`)
            const { playerId, playerName } = message
            players.set(playerId, ws)

            const room = createRoom(playerId, playerName)

            ws.send(
              JSON.stringify({
                type: "roomCreated",
                gameData: room,
              }),
            )

            console.log(`‚úÖ Stanza ${room.roomCode} creata e inviata al client`)
          }
          break

        case "joinRoom":
          {
            console.log(`üö™ ${message.playerName} tenta di unirsi a ${message.roomCode}`)
            const { playerId, playerName, roomCode } = message
            players.set(playerId, ws)

            const room = addPlayerToRoom(roomCode, playerId, playerName)

            if (room) {
              console.log(`‚úÖ ${playerName} unito a ${roomCode}`)

              // Invia aggiornamento al giocatore che si √® unito
              ws.send(
                JSON.stringify({
                  type: "roomJoined",
                  gameData: room,
                }),
              )

              // Invia aggiornamento a tutti i giocatori nella stanza
              broadcastToRoom(roomCode, {
                type: "gameUpdate",
                gameData: room,
              })

              console.log(`üì° Aggiornamenti inviati per stanza ${roomCode}`)
            } else {
              console.log(`‚ùå Impossibile unire ${playerName} a ${roomCode}`)
              ws.send(
                JSON.stringify({
                  type: "error",
                  message: "‚ùå Stanza non trovata!",
                }),
              )
            }
          }
          break

        case "startGame":
          {
            console.log(`üéÆ Richiesta avvio gioco da ${message.playerId} per stanza ${message.roomCode}`)

            const { roomCode, playerId } = message
            const room = rooms.get(roomCode)

            if (!room) {
              console.log(`‚ùå Stanza ${roomCode} non trovata per startGame`)
              ws.send(
                JSON.stringify({
                  type: "error",
                  message: "‚ùå Stanza non trovata!",
                }),
              )
              break
            }

            console.log(`üìä Stanza trovata: ${roomCode}, giocatori: ${room.players.length}`)

            const player = room.players.find((p) => p.id === playerId)
            if (!player) {
              console.log(`‚ùå Giocatore ${playerId} non trovato nella stanza`)
              ws.send(
                JSON.stringify({
                  type: "error",
                  message: "‚ùå Giocatore non trovato nella stanza!",
                }),
              )
              break
            }

            console.log(`üë§ Giocatore trovato: ${player.name}, √® host: ${player.isHost}`)

            if (!player.isHost) {
              console.log(`‚ùå ${player.name} non √® l'host`)
              ws.send(
                JSON.stringify({
                  type: "error",
                  message: "‚ùå Solo l'host pu√≤ iniziare il gioco!",
                }),
              )
              break
            }

            const result = startGame(roomCode)
            console.log(`üéÆ Risultato startGame:`, result)

            if (result && result.success) {
              console.log(`‚úÖ Gioco avviato, invio aggiornamenti...`)

              broadcastToRoom(roomCode, {
                type: "gameUpdate",
                gameData: room,
              })

              console.log(`üì° Aggiornamenti inviati per avvio gioco`)
            } else {
              console.log(`‚ùå Errore avvio gioco:`, result?.error)
              ws.send(
                JSON.stringify({
                  type: "error",
                  message: result?.error || "‚ùå Errore nell'avvio del gioco",
                }),
              )
            }
          }
          break

        case "guess":
          {
            console.log(`üéØ Tentativo di ${message.playerName}: ${message.guess}`)
            // Implementazione guess...
          }
          break

        case "chat":
          {
            const { roomCode, playerId, playerName, message: chatMessage } = message
            const room = rooms.get(roomCode)

            if (room) {
              room.chat.push({
                playerName,
                message: chatMessage,
                timestamp: Date.now(),
              })

              // Mantieni solo gli ultimi 50 messaggi
              if (room.chat.length > 50) {
                room.chat = room.chat.slice(-50)
              }

              broadcastToRoom(roomCode, {
                type: "gameUpdate",
                gameData: room,
              })

              console.log(`üí¨ ${playerName}: ${chatMessage}`)
            }
          }
          break

        case "ping":
          ws.send(JSON.stringify({ type: "pong" }))
          break

        default:
          console.log("‚ùì Tipo messaggio sconosciuto:", message.type)
      }
    } catch (error) {
      console.error("‚ùå Errore parsing messaggio:", error)
      ws.send(
        JSON.stringify({
          type: "error",
          message: "Errore del server: " + error.message,
        }),
      )
    }
  })

  ws.on("close", () => {
    console.log("üîå Connessione WebSocket chiusa")
    // Cleanup logic...
  })

  ws.on("error", (error) => {
    console.error("‚ùå Errore WebSocket:", error)
  })
})

const PORT = process.env.PORT || 3000
const HOST = process.env.HOST || "0.0.0.0"

server.listen(PORT, HOST, () => {
  console.log(`üöÄ Server avviato su ${HOST}:${PORT}`)
  console.log(`üåê Apri http://localhost:${PORT} per giocare`)
})
